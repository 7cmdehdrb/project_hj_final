#!/usr/bin/env python3
"""
Fake MarkerArray publisher for testing intention_transform_node_ros2.py.

Publishes:
- Virtual boxes in map frame
- Real boxes in base_link frame

Real boxes are generated by adding random noise to the virtual box poses in map,
then transforming them into base_link using the current TF between map and base_link.

Required TF:
- map -> base_link must be available from localization or another TF publisher.

If USE_IDENTITY_FALLBACK_WHEN_TF_MISSING is True, identity transform is used when TF
is unavailable. This is only for standalone debugging.
"""

from __future__ import annotations

import copy
from dataclasses import dataclass
from typing import List, Optional

import numpy as np
from scipy.spatial.transform import Rotation as R

import rclpy
from rclpy.duration import Duration
from rclpy.node import Node
from rclpy.time import Time
from rclpy.qos import QoSProfile, qos_profile_system_default

from geometry_msgs.msg import Point, Pose, Quaternion, TransformStamped, Vector3
from std_msgs.msg import ColorRGBA, Header
from tf2_ros import Buffer, TransformException, TransformListener
from visualization_msgs.msg import Marker, MarkerArray

# =============================================================================
# Hard-coded configuration section
# =============================================================================

NODE_NAME = "fake_box_marker_array_publisher"

VIRTUAL_BOX_TOPIC = "/virtual_box"
REAL_BOX_TOPIC = "/real_box"

MAP_FRAME = "map"
BASE_FRAME = "base_link"

PUBLISH_RATE_HZ = 10.0
TF_LOOKUP_TIMEOUT_SEC = 0.05

SUBSCRIPTION_QOS: QoSProfile = qos_profile_system_default
PUBLISHER_QOS: QoSProfile = qos_profile_system_default

# If True, publish real boxes using identity map->base_link when TF is missing.
# For actual integration testing, keep this False so TF problems are visible.
USE_IDENTITY_FALLBACK_WHEN_TF_MISSING = False

# Random noise applied to the virtual box pose in map before transforming to base_link.
# Position noise is Gaussian [m]. Orientation noise is Gaussian [deg] in roll/pitch/yaw.
POSITION_NOISE_STD_M = np.array([0.005, 0.005, 0.005], dtype=float)
ORIENTATION_NOISE_STD_DEG = np.array([2.0, 2.0, 2.0], dtype=float)

# If True, noise is sampled once and then held fixed.
# If False, noise changes every publish cycle.
FIX_NOISE_ON_START = True

# Marker shape and visualization settings.
BOX_SCALE = Vector3(x=0.01, y=0.1, z=0.1)
VIRTUAL_BOX_COLOR = ColorRGBA(r=0.0, g=0.7, b=1.0, a=0.45)
REAL_BOX_COLOR = ColorRGBA(r=1.0, g=0.4, b=0.0, a=0.45)


# =============================================================================
# Input virtual box data
# =============================================================================


@dataclass(frozen=True)
class BoxSeed:
    marker_id: int
    name: str
    x: float
    y: float
    z: float
    qx: float
    qy: float
    qz: float
    qw: float


